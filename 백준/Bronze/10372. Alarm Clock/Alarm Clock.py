n = int(input())
time = {8:'11', 9:'17', 10:'14', 11:'12', 12:'10', 13:'24', 14:'22', 15:'20',
        16:'11', 17:'17', 18:'14', 19:'12', 20:'10', 21:'24', 22:'22',
        23: '20', 24:'00', 25:'08'}
if 8 <= n <= 15:
    print(f'11:{time[n]}')
elif 15 < n <= 25:
    print(f'00:{time[n]}')
elif n == 26:
    print('08:08')    
else:
    print('Impossible')