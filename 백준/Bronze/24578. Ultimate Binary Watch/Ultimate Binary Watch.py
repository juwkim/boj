n = int(input())
a = n // 1000
b = n % 1000 // 100
c = n % 100 // 10
d = n % 10

for i in range(3, -1, -1):
    s = ''
    s += '.*'[a & (1 << i) != 0]
    s += ' '
    s += '.*'[b & (1 << i) != 0]
    s += '   '
    s += '.*'[c & (1 << i) != 0]
    s += ' '
    s += '.*'[d & (1 << i) != 0]
    print(s)