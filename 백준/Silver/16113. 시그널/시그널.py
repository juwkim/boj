N = int(input())
s = input()
d = "####.##.##.####", "#####", "###..#####..###", "###..####..####", "#.##.####..#..#", "####..###..####", "####..####.####", "###..#..#..#..#", "####.#####.####", "####.####..####"
i, l = 0, N // 5
while i < l:
    if s[i] == '.':
        i += 1
        continue
    for num in (0, 2, 3, 4, 5, 6, 7, 8, 9):
        if "".join(s[j:j+3] for j in range(i, N, l)) == d[num]:
            print(num, end="")
            i += 4
            break
    else:
        print(1, end="")
        i += 2