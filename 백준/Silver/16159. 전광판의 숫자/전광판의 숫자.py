dic = {v: k for k, v in enumerate(bitmaps)}

l = [input() for _ in range(7)]
arr = [dic[tuple(l[i][j:j + 6] for i in range(7))] for j in range(0, len(l[0]), 6)]

i = len(arr) - 2
while i >= 0 and arr[i] >= arr[i + 1]:
    i -= 1
if i == -1:
    ans = "The End"
else:
    j = len(arr) - 1
    while arr[j] <= arr[i]:
        j -= 1
    arr[i], arr[j] = arr[j], arr[i]
    arr[i + 1:] = reversed(arr[i + 1:])
    ans = '\n'.join(''.join(bitmaps[num][i] for num in arr) for i in range(7))
