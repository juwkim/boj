rev_dic = {v: k for k, v in dic.items()}

for line in open(0):
    buf = [*map(rev_dic.get, line.rstrip())]
    s = ''.join(buf)
    prev = 0
    for l in [*map(len, buf)][::-1]:
        print(dic[s[prev:prev + l]], end='')
        prev += l
    print()